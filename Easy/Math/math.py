# Functions corresponding to arithmetic and math calculations.

# Write a function that adds any two numbers together
def add(a, b):
    return a + b

# Write a function that subtracts any two numbers together
def subtract(a, b):
    return a - b

# Write a function that multiplies any two numbers together
def multiply(a, b):
    return a * b

# Write a function that divides any two numbers together
def divide(a, b):
    return a / b

# Write a function that returns the value of a to the power of b
def exponent(a, b):
    return a**b